/**
 * Seeds realistic, named sample attendees for the Global AI Expo 2026 event so
 * the scanner, attendee management and check-in screens have data to work with.
 *
 * One confirmed + paid order per ticket type holds its attendees; each paid
 * order carries a payment channel + the project's active gateway so the admin
 * table shows Payment / Mode columns (instead of "-"). ~1 in 7 attendees is
 * pre-checked-in to exercise the check-in UI. Names/emails are explicit so the
 * placeholder ("Tamu #n") path is bypassed. qr_token + ulid are generated by
 * Attendee::boot, so every attendee is scannable.
 *
 * Resettable: every run first hard-deletes its own sentinel batch (orders,
 * items, attendees, scan logs) and recreates it fresh - never touches real
 * public orders. Safe to run on production:
 * php artisan db:seed --class=GlobalAiExpoAttendeesSeeder
 */
class GlobalAiExpoAttendeesSeeder extends Seeder
{
    private const PROJECT_USERNAME = 'globalaiexpo';

    private const BATCH_LABEL = 'Seeder: Global AI Expo Attendees';

    /**
     * Payment channels cycled across paid orders so the Payment column reads
     * like real Xendit virtual-account / QRIS settlements.
     *
     * @var list<string>
     */
    private const CHANNELS = ['BCA', 'QRIS', 'MANDIRI', 'BNI', 'PERMATA'];

    /**
     * Ticket slug => attendee full names. Distribution: 18/8/6/6/4 = 42 total.
     *
     * @var array<string, list<string>>
     */
    private const ATTENDEES = [
        'regular-ticket' => [
            'Budi Santoso', 'Siti Nurhaliza', 'Andi Wijaya', 'Dewi Lestari',
            'Rizki Pratama', 'Putri Maharani', 'Eko Saputra', 'Rina Kartika',
            'Agus Setiawan', 'Maya Anggraini', 'Fajar Nugroho', 'Indah Permata',
            'Hendra Gunawan', 'Lia Marlina', 'Doni Kurniawan', 'Sri Wahyuni',
            'Bayu Aji', 'Nadia Safitri',
        ],
        'vip-ticket' => [
            'Reza Pahlevi', 'Anita Salim', 'Bambang Sutrisno', 'Clara Wijoyo',
            'Gilang Ramadhan', 'Vania Kusuma', 'Yusuf Hidayat', 'Tania Pertiwi',
        ],
        '3-day-bundle' => [
