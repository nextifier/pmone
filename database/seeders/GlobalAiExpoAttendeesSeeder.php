    public function run(): void
    {
        $event = Event::query()
            ->whereHas('project', fn ($q) => $q->where('username', self::PROJECT_USERNAME))
            ->where('is_active', true)
            ->latest()
            ->first();

        if (! $event) {
            $this->command?->warn('Global AI Expo event not found; nothing to seed.');

            return;
        }

        $this->purgeBatch($event);

        $purchases = app(TicketPurchaseService::class);
        $gateway = $event->project?->activePaymentGateway();
        $staffId = $this->resolveStaffId();
        $created = 0;
        $checkedIn = 0;
        $globalIndex = 0;
        $orderIndex = 0;

        DB::transaction(function () use ($event, $purchases, $gateway, $staffId, &$created, &$checkedIn, &$globalIndex, &$orderIndex) {
            foreach (self::ATTENDEES as $slug => $names) {
                $ticket = Ticket::query()
                    ->where('event_id', $event->id)
                    ->where('slug', $slug)
                    ->first();

                if (! $ticket) {
                    $this->command?->warn("Ticket '{$slug}' not found; skipping.");

                    continue;
                }

                $ticket->loadMissing('pricePhases');
                $phase = $purchases->resolveActivePhase($ticket);
                $unitPrice = $phase ? (float) $phase->price : 0.0;
                $quantity = count($names);
                $total = $unitPrice * $quantity;
                $isFree = $total <= 0.0;

                $order = TicketOrder::query()->create([
                    'event_id' => $event->id,
                    'status' => TicketOrderStatus::Confirmed,
                    'buyer_name' => $names[0],
                    'buyer_email' => $this->emailFor($names[0]),
                    'buyer_phone' => '0811'.str_pad((string) random_int(0, 9999999), 7, '0', STR_PAD_LEFT),
                    'subtotal' => $total,
                    'discount_amount' => 0,
                    'total' => $total,
                    'payment_channel' => $isFree ? null : self::CHANNELS[$orderIndex % count(self::CHANNELS)],
                    'payment_gateway_id' => $isFree ? null : $gateway?->id,
                    'payment_ref' => $isFree ? null : 'SEED-'.Str::upper(Str::random(10)),
                    'paid_at' => now(),
                    'source' => 'seeder',
                    'batch_label' => self::BATCH_LABEL,
                ]);
                $orderIndex++;

                $item = $order->items()->create([
                    'ticket_id' => $ticket->id,
                    'quantity' => $quantity,
                    'unit_price' => $unitPrice,
                    'phase_label' => $phase?->label,
                    'subtotal' => $total,
                ]);

                foreach ($names as $name) {
                    $globalIndex++;
                    $isCheckedIn = $globalIndex % 7 === 0;

                    $item->attendees()->create([
                        'ticket_id' => $ticket->id,
                        'name' => $name,
                        'email' => $this->emailFor($name),
                        'personalized_at' => now(),
                        'checked_in_at' => $isCheckedIn ? now() : null,
                        'checked_in_by' => $isCheckedIn ? $staffId : null,
                        'checkin_event_id' => $isCheckedIn ? $event->id : null,
                    ]);

                    $created++;
                    $checkedIn += $isCheckedIn ? 1 : 0;
                }
            }
        });

        $this->command?->info("Seeded {$created} attendees for Global AI Expo 2026 ({$checkedIn} checked-in).");
    }

    /**
     * Hard-delete this seeder's previous sentinel batch (orders, items,
     * attendees, scan logs) so a re-run produces fresh, scannable data. Scoped
     * strictly to the sentinel batch_label - real public orders are untouched.
     */
    private function purgeBatch(Event $event): void
    {
        $orderIds = TicketOrder::withTrashed()
            ->where('event_id', $event->id)
            ->where('batch_label', self::BATCH_LABEL)
            ->pluck('id');

        if ($orderIds->isEmpty()) {
            return;
        }

        $itemIds = TicketOrderItem::withTrashed()
            ->whereIn('ticket_order_id', $orderIds)
            ->pluck('id');

        $attendeeIds = Attendee::withTrashed()
            ->whereIn('ticket_order_item_id', $itemIds)
            ->pluck('id');

        DB::transaction(function () use ($orderIds, $itemIds, $attendeeIds) {
            ScanLog::query()->whereIn('attendee_id', $attendeeIds)->delete();
            Attendee::withTrashed()->whereIn('id', $attendeeIds)->forceDelete();
            TicketOrderItem::withTrashed()->whereIn('id', $itemIds)->forceDelete();
            TicketOrder::withTrashed()->whereIn('id', $orderIds)->forceDelete();
        });

        $this->command?->info("Purged {$orderIds->count()} previously seeded order(s).");
    }

    /**
     * A staff/admin user id to stamp on pre-checked-in attendees, or null.
     */
    private function resolveStaffId(): ?int
    {
        $staff = User::query()
            ->whereHas('roles', fn ($q) => $q->whereIn('name', ['master', 'admin', 'staff']))
            ->value('id');

        return $staff ?? User::query()->value('id');
    }

    private function emailFor(string $name): string
    {
        $handle = Str::of($name)->lower()->ascii()->replaceMatches('/[^a-z]+/', '.')->trim('.');

        return "{$handle}@example.com";
    }
}
